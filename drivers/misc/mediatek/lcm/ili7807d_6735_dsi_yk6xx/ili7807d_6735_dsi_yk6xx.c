static unsigned int lcm_compare_id(void)
{
	unsigned int id=0;
	unsigned char buffer[3];
	unsigned int array[16];  

	SET_RESET_PIN(1);  //NOTE:should reset LCM firstly
	MDELAY(10);
	SET_RESET_PIN(0);
	MDELAY(10);
	SET_RESET_PIN(1);
	MDELAY(120);	

	array[0]=0x00063902;
	array[1]=0x068198FF;//1>6
	dsi_set_cmdq(array, 2, 1);
	MDELAY(10); 

	//array[0] = 0x00023700;// set return byte number
	array[0] = 0x00023700;// set return byte number
	dsi_set_cmdq(array, 1, 1);

	read_reg_v2(0xF0, buffer, 1);
	read_reg_v2(0xF1, buffer+1, 1);

	//zrl modify for ili9806 read ID,121113       
	id = buffer[0]<<8 |buffer[1];


#if defined(BUILD_LK)
	//printf("ili7807c 0x%x , 0x%x , 0x%x \n",buffer[0],buffer[1],id);
#else
	//printk("ili7807c 0x%x , 0x%x , 0x%x \n",buffer[0],buffer[1],id);
#endif
   //      return 1;
       return (id == LCM_ID_ILI7807C)?1:0;
     
}                                     

