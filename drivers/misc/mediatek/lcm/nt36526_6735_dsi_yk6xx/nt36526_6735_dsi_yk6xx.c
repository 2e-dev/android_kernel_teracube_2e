static void lcm_init(void)
{
	#ifdef CONFIG_MTK_LCM_5V_IC
	unsigned char cmd = 0x0;
	unsigned char data = 0xFF;
#ifndef CONFIG_FPGA_EARLY_PORTING
	int ret = 0;
#endif
	cmd = 0x00;
	data = 0x12;//5.8v
	SET_RESET_PIN(0);
#ifndef BUILD_LK 
	//set_gpio_lcd_enp(1);
	set_gpio_lcm_enp_enn(1);
#endif
#ifdef BUILD_LK
	ret = TPS65132_write_byte(cmd, data);
#else
	ret = tps65132_write_bytes(cmd, data);
#endif
	cmd = 0x01;
	data =  0x12;//5.8v
#ifdef BUILD_LK
	ret = TPS65132_write_byte(cmd, data);
#else
	ret = tps65132_write_bytes(cmd, data);
#endif
#else
#ifdef BUILD_LK
	int ret = 0;

	SET_RESET_PIN(1);
	ret = PMU_REG_MASK(0xB0, 0x00, 0x01);
	//ret = PMU_REG_MASK(0xB2, 0x28, 0x3f);
	ret = PMU_REG_MASK(0xB3, 0x24, 0x3F);

	ret = PMU_REG_MASK(0xB4, 0x24, 0x3F);

	ret = PMU_REG_MASK(0xB1, 0x40, 0x48);

	MDELAY(15);
	ret = PMU_REG_MASK(0xB1, 0x48, 0x48);
	MDELAY(2);

#endif
#ifndef BUILD_LK	
	display_bias_enable();
	MDELAY(2);
#endif
#endif
	SET_RESET_PIN(1);	 
	MDELAY(10); //20); 
	SET_RESET_PIN(0);
	MDELAY(20); //50); 
	SET_RESET_PIN(1);
	MDELAY(50); //150);

#ifdef BUILD_LK //added by xen for softlink lk&kernel lcm driver 20171020
 	push_table(lcm_initialization_setting, sizeof(lcm_initialization_setting) / sizeof(struct LCM_setting_table), 1);
#else
	push_table(NULL, lcm_initialization_setting, sizeof(lcm_initialization_setting) / sizeof(struct LCM_setting_table), 1);
#endif

}

