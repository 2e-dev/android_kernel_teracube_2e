static unsigned int lcm_compare_id(void)
{

        unsigned int id=0;//,id2=0;
        unsigned char buffer[5]={0};//,buffer2[2];
        unsigned int array[2]={0};  

        SET_RESET_PIN(1);
        SET_RESET_PIN(0);
        MDELAY(25);
        SET_RESET_PIN(1);
        MDELAY(50);

        array[0]=0x00043902;
        array[1]=0x011980ff;
        dsi_set_cmdq(array, 2, 1); //{0xff, 3 ,{0x80,0x19,0x01}}, // Command2 Enable
       
        array[0]=0x80001500;
        dsi_set_cmdq(array, 1, 1); //{0x00, 1 ,{0x80}},
       
        array[0]=0x00033902;
        array[1]=0x001980ff;
        dsi_set_cmdq(array, 2, 1); //{0xff, 2 ,{0x80,0x19}}, // Orise Mode Enable
  
        array[0] = 0x00053700;// set return byte number
        dsi_set_cmdq(array, 1, 1);
        read_reg_v2(0xA1, buffer, 5); // Read Register 0xA1 : 0x80,0x09/0x01,0x8B (OTM8009A/OTM8018B);

        id = buffer[2]<<8 |buffer[3];
        return (id==LCM_ID_OTM8019A)?1:0;

}

